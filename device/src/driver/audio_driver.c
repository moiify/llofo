int audio_saveAudio(uint8_t *data, int data_len, uint8_t audio_index, uint8_t isCustomed)
{
    if(!data || data_len < 5)
    {
        return -1;
    }
    const char *audioType = audio_getAudioTypeFromData(data, data_len);
    if(audioType == AUDIO_TYPE_NULL)
    {
        return -1;
    }
    char filename[MAX_FILENAME_LEN];
    if(isCustomed)
    {
        snprintf(filename, MAX_FILENAME_LEN, "%sdt%d.%s", FILE_AUDIO_DIR, audio_index, audioType);
    }
    else
    {
        snprintf(filename, MAX_FILENAME_LEN, "%s%d.%s", FILE_AUDIO_DIR, audio_index, audioType);
    }

    return fs_saveFileData((const char *)filename, data, data_len);
}

