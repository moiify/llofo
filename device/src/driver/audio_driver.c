int audio_initialAudios(AudioDef_st *pAudios, int num)
{
    u8 type = 0;
    u8 filename[MAX_FILENAME_LEN] = {0};
    for(int i = 0; i < num; i++)
    {
        if(pAudios[i].dat && pAudios[i].len)
        {
            u8 ret = audio_AudioFileExist(pAudios[i].index, &type);
            if( ret == 0) //查找文件系统中相同idx的文件,包括定制
            {
                audio_saveAudio((uint8_t *)pAudios[i].dat, pAudios[i].len, pAudios[i].index, 0);  //如果没有,就保存
            }
            else if(ret == 1)//如果有,存在的是默认语音,对比CRC,如果相同则不保存,不相同则删除原来的,保存现有的
            {
                u16 crcNew = crc16_ccitt((const char *)pAudios[i].dat, pAudios[i].len);
                u16 crcOld = 0;

                switch(type)
                {
                    case 1:
                    {
                        snprintf((char *)filename, MAX_FILENAME_LEN, "%s%d.amr", FILE_AUDIO_DIR, pAudios[i].index);
                    }
                    break;
                    case 2:
                    {
                        snprintf((char *)filename, MAX_FILENAME_LEN, "%s%d.mp3", FILE_AUDIO_DIR, pAudios[i].index);
                    }
                    break;
                    case 3:
                    {
                        snprintf((char *)filename, MAX_FILENAME_LEN, "%s%d.wav", FILE_AUDIO_DIR, pAudios[i].index);
                    }
                    break;
                    default:
                        break;
                }
                int filelen = fs_getFileSize((const char *)filename);
                if(filelen > 0)
                {
                    u8 *filedata = Malloc(filelen);
                    if(fs_getFileData((const char *)filename, filedata, filelen) == 0)
                    {
                        crcOld = crc16_ccitt((const char *)filedata, filelen);
                    }
                    Free(filedata);
                    if(crcNew != crcOld)
                    {
                        audio_deleteDefaultAudio(pAudios[i].index);
                        audio_saveAudio((uint8_t *)pAudios[i].dat, pAudios[i].len, pAudios[i].index, 0);
                    }
                    else
                    {
                        continue;
                    }
                }
                else
                {
                    LOG_ERROR("[audio driver] get default audio file Error!");
                }
            }
            else if(ret == 2) //如果有,如果存在的是定制语音，则不保存
            {
                break;
            }
        }
    }
#if defined(APP_RELEASE) //升级包不删除文件系统已存在语音

#else
    int j,k;
    for(j = 0; j < AUDIO_MAX_NUMBER; j++)  //删除文件系统里有,但是固件里没有的默认语音
    {
        for(k = 0; k < num; k++)
        {
            if(pAudios[k].index == j && pAudios[k].dat && pAudios[k].len)
            {
                break;
            }
        }
        if(k == num)
        {
            audio_deleteDefaultAudio(j);
        }
    }
#endif
    return 0;
}

