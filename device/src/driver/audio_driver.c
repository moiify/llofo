#define MAX_AUDIO_COUNT (5)
typedef struct queue
{
    int front;
    int rear;
    int audio[MAX_AUDIO_COUNT];
} AUDIO_QUEUE;

static AUDIO_QUEUE audio_queue;

/*
 * to judge whether the queue is full
 */
L_BOOL audio_isAudioQueueFull(void)
{
    return (L_BOOL)(audio_queue.front == (audio_queue.rear + 1) % MAX_AUDIO_COUNT);
}

/*
 * to judge whether the queue is empty
 */
L_BOOL audio_isAudioQueueEmpty(void)
{
    return (L_BOOL)(audio_queue.front == audio_queue.rear);
}

/*
 * get the queue's actual size
 */
int audio_Audiosize(void)
{
    return (audio_queue.rear - audio_queue.front + MAX_AUDIO_COUNT) % MAX_AUDIO_COUNT;
}

/*
 * put a AUDIO in the queue
 */
L_BOOL audio_enqueueAudio(int audio_index)
{
    if(audio_isAudioQueueFull())
    {
        return L_FALSE;
    }

    audio_queue.audio[audio_queue.rear] = audio_index;

    audio_queue.rear = (audio_queue.rear + 1) % MAX_AUDIO_COUNT;

    return L_TRUE;
}

/*
 * get a AUDIO in the queue
 */
L_BOOL audio_dequeueAudio(int *audio_index)
{
    if(audio_isAudioQueueEmpty() || g_isAudioPlaying || fibo_get_audio_status())
    {
        return L_FALSE;
    }

    *audio_index = audio_queue.audio[audio_queue.front];

    audio_queue.front = (audio_queue.front + 1) % MAX_AUDIO_COUNT;

    return L_TRUE;
}

int audio_playAudio(int audio_index, int isForce)
{
    if(isForce)
    {
        return audio_play(audio_index);
    }
    return audio_enqueueAudio(audio_index);
}

