static L_BOOL audio_isInNightSection(void)//判断是否在晚上
{
    if(g_pIsNightVoiceOn && *g_pIsNightVoiceOn)
    {
        uint32_t timestamp = (uint32_t)rtc_getTimestamp();
        uint32_t realTime = (timestamp + 28800) % 86400;
        if(timestamp > 1546272000 && (realTime >= 72000 || realTime <= 25200))// 2019-01-01 00:00:00 尚未同步时间戳
        {
            return L_TRUE; //晚上八点到早上七点
        }
    }
    return L_FALSE;
}


