static int audio_checkAudioPlaying(void)
{
    if(g_isAudioPlaying == L_TRUE)
    {
        if(audioBeepTime++ >= MAX_AUDIO_TIME || !fibo_get_audio_status()) //playtone时 fibo_get_audio_status 返回空闲
        {
            if(g_isTonePlaying != L_TRUE)  //如果在播放语音文件
            {
                audioBeepTime = 0;
                audio_play_finish();
            }
            else //正在播放tone
            {
                if(audioBeepTime >= MAX_AUDIO_TIME)  //播放tone超时
                {
                    g_isAudioPlaying = L_FALSE;
                    g_isTonePlaying = L_FALSE;
                    fibo_gpio_set(PIN_SPK_CTRL, GPIO_LEVEL_LOW);
                    LOG_DEBUG("[Audio] Tone Play Finish");
                }
            }
        }
    }
    return 0;
}

